/**
 * Открытие бургерного меню
 * @param {htmlElement} element
 */
function burgerMenuOpen (element) {
  element.classList.add('');
}

/**
 * Закрытие бургерного меню
 * @param {htmlElement} element 
 */
function burgerMenuClose (element) {
  element.classList.remove('');
}